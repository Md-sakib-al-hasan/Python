class Calculator:
    def add(self,a,b):
        return a+b
    def remo(self,a,b):
        return a-b
    def mul(self,a,b):
        return a*b
    def div(self,a,b):
        return a//b

result= Calculator()
print(result.add(5,4))
print(result.remo(10,1))
print(result.mul(2,3))
print(result.div(9,3))
        
